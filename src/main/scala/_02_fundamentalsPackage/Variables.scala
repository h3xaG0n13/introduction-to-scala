package _02_fundamentalsPackage

object Variables extends App {

  /*
    Variable: A variable is a placeholder for storing a value of particular type.
              Unlike values, a variable is mutable. So the assigned values can be changed.
              Similar to Values, explicit definition of type is optional.
   */

  var aInt: Int = 1
  var bInt = 2

  println(aInt)
  println(bInt)
}
