package _02_fundamentalsPackage

object Values extends App{

  /*
    Value:  A Value is similar to a constant in other programming languages.
            Values are immutable, hence a value once assigned cannot be changed.
            Type of a value is optional as compiler can infer it based on assignment.
   */

  val aInt: Int = 1
  val bInt = 2

  println(aInt)
  println(bInt)
}
